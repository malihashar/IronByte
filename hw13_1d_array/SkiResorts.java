public class SkiResorts {
    public static void main(String[] args) {
        String skiResorts[] = {
            "Whistler Blackcomb", "Squaw Valley", "Brighton", "Snowmass", "Sun Valley", "Taos"
        };

        System.out.println("a) Index of Brighton: " + 2);
        System.out.println("b) Expression: skiResorts[2]");
        System.out.println("c) Length of array: " + skiResorts.length);
        System.out.println("d) Index of last item: " + (skiResorts.length - 1));
        System.out.println("e) Value at index 4: " + skiResorts[4]);
    }
}
